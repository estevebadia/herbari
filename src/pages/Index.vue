<div class="text-h6">PIUS FONT I QUER, ELS ANYS DE JOVENTUT</div>
<p>
L’insigne botànic Pius Font i Quer va néixer a Lleida el 1888. Als quatre anys es va traslladar a Manresa per motius professionals del pare que era farmacèutic. Va passar en aquesta ciutat la major part de la vida d’infant i la primera joventut i hi va fer els estudis primaris i de batxillerat. A la capital del Bages va fer amistat amb nois amb els quals aviat va començar a agafar interès per la natura i per les excursions. Amb ells va adquirir l’esperit d’explorador que el caracteritzava. L’any 1905, quan només tenia 17 anys, va ser un dels socis fundadors del Centre Excursionista de la Comarca de Bages, i tota la vida va conservar el tracte amical amb els companys manresans amb qui havia començat a conèixer els variats paisatges del territori bagenc. L’any 1914 va ser distingit com a fill adoptiu de Manresa, on hi ha un institut d'ensenyament secundari que duu el seu nom.
</p>
<p>
Pius Font i Quer es llicencià en ciències químiques a la Universitat de Barcelona el 1908, i el mateix any va assistir al curset de botànica per a excursionistes que feia el doctor Manuel Llenas a la ICHN. Segons reconeixia més endavant, el curset del doctor Llenas va fer que l’esperit excursionista, que havia adquirit ja feia temps, derivés definitivament cap a la botànica científica. Un cop orientat cap a l’estudi de les plantes, va fer la carrera de farmàcia, que incloïa una assignatura de botànica, i l’any 1910 va obtenir el títol de farmacèutic. Amb el desig de tenir l’oportunitat d’estendre el treball botànic a un espai ben ampli, va fer oposicions per entrar a la farmàcia militar, i les va guanyar l’any 1911. Això el va portar a Madrid, a Burgos i, de 1913 a 1914, a Maó, cosa que li va permetre conèixer flores molt variades. Com que l’obra botànica més completa que havia fet fins llavors era l’estudi de la comarca de Bages titulat Ensayo Fitotopográfico de Bages, la va presentar com a tesi de doctorat l’any 1914 a la Universitat de Madrid, l’única que, en aquell temps, podia donar el títol de doctor. L’obra la va presentar en forma de llibre ja imprès (editat a Maó el mateix any 1914). Com que llavors no era permès acceptar obres ja publicades com a tesis doctorals, va haver de tornar a escriure tot el text per tal que fos admès.
</p>
<p>
Pius Font i Quer va ser president de la Institució Catalana d’Història Natural de l’any 1931 al 1934.
</p>
