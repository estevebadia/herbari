<div class="text-h6">L’HERBARI DEL BAGES</div>
<p>
Pius Font i Quer investigà i herboritzà intensament les plantes de la comarca de Bages durant els anys 1909 i 1910, abans que els límits comarcals fossin establerts pel geògraf Pau Vila. Fruit d’aquesta recerca i de l’acurada metodologia de Font i Quer és l’Herbari del Bages que l’autor va seguir ampliant fins al 1912.
</p>
<p>
L’Herbari del Bages comprèn 628 plecs o mostres que corresponen a 618 espècies de plantes; la majoria d’espècies tenen un únic plec, unes poques en tenen 2 i una sola espècie en té 3. Cadascun dels plecs o mostres conserva l’etiqueta original de Pius Font i Quer amb el seu nom científic, la localitat i la data de recollida, a vegades un nom vulgar i esporàdicament un comentari. Els plecs de l’Herbari del Bages s’ordenen per famílies i posteriorment en 10 arxivadors.
</p>
<p>
Pius Font i Quer diposità l’Herbari del Bages al Centre Excursionista de la Comarca de Bages, del qual era membre i soci fundador. Cal suposar que per una especial estima al CECB, perquè l’Herbari del Bages constitueix una unitat sobre flora del Bages o per ambdues raons alhora, Font i Quer no s’endugué anys més tard l’Herbari del Bages amb ell a l’Institut Botànic de Barcelona. Durant més d’un segle, l’Herbari del Bages s’ha mantingut a la biblioteca del CECB, a Manresa.
</p>
<p>
L’any 1914, Font i Quer publicava el seu treball Ensayo Fitotopográfico de Bages, la flora del Bages que li valdria el títol de doctor en farmàcia. L’Herbari del Bages constitueix també en bona mesura el recull de mostres que avalen i permeten contrastar les dades florístiques presentades a l’Ensayo Fitotopográfico de Bages.
</p>
<p>
L’any 2021, el CECB va decidir donar l’Herbari del Bages a l’Ajuntament de Manresa qui, al seu torn, pretén cedir-lo en comodat al Centre de Documentació de la Biodiversitat Vegetal (CeDocBiV), un servei científic integrat en el Centre de Recursos per a l’Aprenentatge i la Investigació (CRAI) de la Universitat de Barcelona. L’octubre del 2024 es va materialitzar aquesta cessió. Agraïm a la direcció i al personal del CeDocBiV la seva predisposició per integrar l’Herbari del Bages a les col·leccions que manté, assegurant-ne la conservació i eixamplant les possibilitats d’ús en recerca botànica i en recerca sobre la història de l’estudi de la botànica a Catalunya.
</p>

<div class="text-h6">L’HERBARI DIGITAL</div>
<p>
Abans de moure l’Herbari del Bages de Pius Font i Quer del CECB al CeDocBiV, una comissió del CECB i de la delegació del Bages de la Institució Catalana d’Història Natural n’hem fet l’inventari, hem fotografiat cadascuna de les mostres, hem buscat la correspondència amb el nom científic actual quan l’original de Font i Quer ha canviat i presentem l’herbari en aquest format digital de consulta fàcil i oberta.
</p>
<p>
El web conté la fotografia digital a resolució alta de cadascun dels plecs de l’Herbari del Bages. Un índex inicial els ordena alfabèticament pel nom científic amb el qual Pius Font i Quer els identificà. Un cercador al capdamunt de l’índex permet buscar indistintament pel nom científic en temps d’un Font i Quer jove, pel nom científic actual o bé pel nom en català de la família. Cada plec porta associada una etiqueta digital que, al nom científic i al nom de la família originals de Font i Quer, afegeix el nom científic i el nom de la família actuals fonamentats en l’edició del 2005 de la Flora Manual dels PPCC. En uns pocs plecs han calgut comentaris per aclarir l’actualització de la nomenclatura. L’etiqueta digital té un botó per visualitzar en una finestra a part la imatge del plec d’herbari prescindint de l’etiqueta digital. A la majoria de plecs trobareu també un botó que enllaça amb l’espècie presentada al web El medi Natural del Bages, igualment de la delegació del Bages de la ICHN. 
</p>
<p>
Amb aquest format digital volem reconèixer a qui va estudiar amb deteniment i vàlua científica la flora del Bages i pretenem que, més d’un segle després d’haver-se creat, l’Herbari del Bages de Pius Font i Quer tingui una nova vida i sigui útil per la recerca botànica.
</p>
